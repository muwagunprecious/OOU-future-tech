async function sendEmails() {
    console.log("Starting script parse...");
    const lines = rawData.split('\n').map(l => l.trim()).filter(Boolean);
    const attendees = [];

    for (let i = 0; i < lines.length; i += 3) {
        if (i + 1 >= lines.length) break;
        const name = lines[i];
        const email = lines[i + 1];
        let type = 'Standard';
        let ticketId = '#OOU-0000';

        if (i + 2 < lines.length) {
            const parts = lines[i + 2].split('\t').map(p => p.trim());
            if (parts[0]) type = parts[0];
            if (parts[1]) ticketId = parts[1];
        }

        attendees.push({ name, email, type, ticketId });
    }

    console.log("Found " + attendees.length + " attendees to email. Sending slowly...");

    for (const person of attendees) {
        try {
            console.log("Sending to " + person.email + " [" + person.ticketId + "]...");
            const response = await fetch('http://localhost:3001/api/send-ticket', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    email: person.email,
                    name: person.name,
                    ticketId: person.ticketId,
                    type: person.type
                })
            });
            const data = await response.json();
            if (!response.ok) {
                console.error("Failed for " + person.email + ": ", data);
            } else {
                console.log("✅ Sent to " + person.email);
            }
        } catch (err) {
            console.error("Error connecting for " + person.email + ": ", err.message);
        }
        // sleep for 100ms to avoid overwhelming the SMTP server
        await new Promise(r => setTimeout(r, 100));
    }
    console.log('✅✅ All emails processed!');
}

